import Link from "next/link";

/**
 * Ячейка таблицы, кликабельная целиком.
 *
 * Зачем: если ссылку повесить только на текст, клик по пустому месту ячейки не работает,
 * хотя строка выглядит кликабельной. Здесь ссылка занимает всю площадь td.
 *
 * Тонкость: `h-px` на td — не опечатка. Строка обычно выше содержимого (первая колонка
 * двухстрочная), а блочная ссылка по высоте не тянется — внизу ячейки оставалась бы
 * мёртвая полоса. Явная высота у td позволяет `h-full` у ссылки посчитаться от реальной
 * высоты строки; сам td при этом всё равно растянется по контенту.
 *
 * Внутрь нельзя класть другие ссылки или кнопки — вложенная интерактивность ломает
 * разметку и доступность. Для таких колонок (телефон, действия) используйте обычный td.
 */
export function Cell({
  href,
  children,
  className = "",
}: {
  href: string;
  children: React.ReactNode;
  className?: string;
}) {
  return (
    <td className="h-px p-0">
      <Link href={href} className="flex h-full items-center px-[14px] py-[13px]">
        <div className={`w-full ${className}`}>{children}</div>
      </Link>
    </td>
  );
}
